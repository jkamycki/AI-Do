import { useState, useEffect, useRef } from "react";
import { useMutation, useQuery } from "@tanstack/react-query";
import { useAuth, useUser } from "@clerk/react";
import { useRoute } from "wouter";
import { useWorkspace } from "@/contexts/WorkspaceContext";
import { PhotoUploadSection } from "@/components/InvitationCustomization/PhotoUploadSection";
import { ColorSystemSection } from "@/components/InvitationCustomization/ColorSystemSection";
import { DesignOptionsSection } from "@/components/InvitationCustomization/DesignOptionsSection";
import { SaveTheDatePreview } from "@/components/InvitationCustomization/SaveTheDatePreview";
import { DigitalInvitationPreview } from "@/components/InvitationCustomization/DigitalInvitationPreview";
import { Card } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { useToast } from "@/hooks/use-toast";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { Download, Loader2 } from "lucide-react";
import type {
  InvitationCustomization,
  ColorPalette,
  PreviewTab,
  WeddingProfileData,
  TextOverrides,
} from "@/types/invitations";

interface RouteParams {
  profileId?: string;
}

interface InvitationCustomizationProps {
  profileId?: number;
}

export default function InvitationCustomizationPage({ profileId: propProfileId }: InvitationCustomizationProps = {}) {
  const { getToken } = useAuth();
  const { user } = useUser();
  const { toast } = useToast();
  const { activeWorkspace } = useWorkspace();
  const [, params] = useRoute<RouteParams>("/guests/:profileId");

  // Get profileId from prop (when used as sub-component), route params, or workspace context
  const profileId = propProfileId || (params?.profileId
    ? parseInt(params.profileId)
    : activeWorkspace?.profileId);
  const [previewTab, setPreviewTab] = useState<PreviewTab>("saveTheDate");

  // State for customizations
  const [primaryColor, setPrimaryColor] = useState("#D4A017");
  const [autoGeneratedPalette, setAutoGeneratedPalette] = useState<ColorPalette>({
    primary: "#D4A017",
    secondary: "#F5C842",
    accent: "#7B2FBE",
    neutral: "#666666",
  });
  const [customColors, setCustomColors] = useState<Partial<ColorPalette> | null>(
    null
  );
  const [selectedPalette, setSelectedPalette] = useState<string | null>(null);
  const [backgroundColor, setBackgroundColor] = useState<string | null>(null);

  const [saveTheDatePhotoFile, setSaveTheDatePhotoFile] = useState<File | null>(
    null
  );
  const [saveTheDatePhotoUrl, setSaveTheDatePhotoUrl] = useState<string | null>(
    null
  );
  const [digitalInvitationPhotoFile, setDigitalInvitationPhotoFile] = useState<
    File | null
  >(null);
  const [digitalInvitationPhotoUrl, setDigitalInvitationPhotoUrl] = useState<
    string | null
  >(null);

  const [saveTheDateFont, setSaveTheDateFont] = useState("Georgia");
  const [digitalInvitationFont, setDigitalInvitationFont] = useState("Georgia");
  const [selectedLayout, setSelectedLayout] = useState("classic");
  const [backgroundImageUrl, setBackgroundImageUrl] = useState<string | null>(
    null
  );
  const [textOverrides, setTextOverrides] = useState<TextOverrides>({});

  const [isDownloadingPDF, setIsDownloadingPDF] = useState(false);
  const saveTheDatePreviewRef = useRef<HTMLDivElement>(null);
  const digitalInvitationPreviewRef = useRef<HTMLDivElement>(null);

  // Track local blob: URLs so we can revoke them when replaced/unmounted.
  const saveTheDateBlobUrlRef = useRef<string | null>(null);
  const digitalInvitationBlobUrlRef = useRef<string | null>(null);

  useEffect(() => {
    return () => {
      if (saveTheDateBlobUrlRef.current) URL.revokeObjectURL(saveTheDateBlobUrlRef.current);
      if (digitalInvitationBlobUrlRef.current) URL.revokeObjectURL(digitalInvitationBlobUrlRef.current);
    };
  }, []);

  const authedFetch = async (url: string, init: RequestInit = {}) => {
    const token = await getToken();
    return fetch(url, {
      ...init,
      credentials: "include",
      headers: {
        "Content-Type": "application/json",
        ...(init.headers ?? {}),
        ...(token ? { Authorization: `Bearer ${token}` } : {}),
      },
    });
  };

  // Fetch wedding profile
  const { data: weddingProfile, isLoading: isLoadingProfile } = useQuery({
    queryKey: ["wedding-profile", profileId],
    queryFn: async () => {
      if (!profileId) return null;
      const r = await authedFetch(`/api/profile`);
      if (!r.ok) throw new Error("Failed to fetch profile");
      return r.json() as Promise<WeddingProfileData>;
    },
    enabled: !!profileId,
    retry: 1,
  });

  // Fetch customizations
  const { data: customization } = useQuery({
    queryKey: ["invitation-customizations", profileId],
    queryFn: async () => {
      if (!profileId) return null;
      const r = await authedFetch(
        `/api/invitation-customizations?profileId=${profileId}`
      );
      if (!r.ok) throw new Error("Failed to fetch customizations");
      return r.json() as Promise<InvitationCustomization>;
    },
    enabled: !!profileId,
  });

  // Load customizations into state
  useEffect(() => {
    if (customization) {
      setPrimaryColor(customization.primaryColor);
      setAutoGeneratedPalette(customization.colorPalette);
      setCustomColors(customization.customColors);
      setSelectedPalette(customization.selectedPalette);
      setBackgroundColor(customization.backgroundColor);
      setSaveTheDatePhotoUrl(customization.saveTheDatePhotoUrl);
      setDigitalInvitationPhotoUrl(customization.digitalInvitationPhotoUrl);
      setSaveTheDateFont(customization.saveTheDateFont || customization.selectedFont || "Georgia");
      setDigitalInvitationFont(customization.digitalInvitationFont || customization.selectedFont || "Georgia");
      setSelectedLayout(customization.selectedLayout);
      setBackgroundImageUrl(customization.backgroundImageUrl);
      setTextOverrides(customization.textOverrides || {});
    }
  }, [customization]);


  // Upload photo mutation
  const uploadPhotoMutation = useMutation({
    mutationFn: async ({
      file,
      type,
    }: {
      file: File;
      type: "save-the-date" | "digital-invitation";
    }) => {
      const formData = new FormData();
      formData.append("file", file);

      const token = await getToken();
      const r = await fetch(
        `/api/invitation-customizations/upload?type=${type}`,
        {
          method: "POST",
          credentials: "include",
          headers: {
            ...(token ? { Authorization: `Bearer ${token}` } : {}),
          },
          body: formData,
        }
      );

      if (!r.ok) {
        const error = await r.json().catch(() => ({ error: "Unknown error" }));
        throw new Error(error.error || `Upload failed with status ${r.status}`);
      }
      return r.json() as Promise<{ url: string }>;
    },
    onSuccess: (data, variables) => {
      if (variables.type === "save-the-date") {
        if (saveTheDateBlobUrlRef.current) {
          URL.revokeObjectURL(saveTheDateBlobUrlRef.current);
          saveTheDateBlobUrlRef.current = null;
        }
        setSaveTheDatePhotoUrl(data.url);
        setSaveTheDatePhotoFile(null);
      } else {
        if (digitalInvitationBlobUrlRef.current) {
          URL.revokeObjectURL(digitalInvitationBlobUrlRef.current);
          digitalInvitationBlobUrlRef.current = null;
        }
        setDigitalInvitationPhotoUrl(data.url);
        setDigitalInvitationPhotoFile(null);
      }
    },
    onError: (error) => {
      toast({
        title: "Upload Failed",
        description: error instanceof Error ? error.message : "Failed to upload photo",
        variant: "destructive",
      });
    },
  });

  // Photo selection handlers: immediately show a local preview, then upload.
  const handleSaveTheDatePhotoSelected = (file: File | null) => {
    if (saveTheDateBlobUrlRef.current) {
      URL.revokeObjectURL(saveTheDateBlobUrlRef.current);
      saveTheDateBlobUrlRef.current = null;
    }

    if (!file) {
      setSaveTheDatePhotoFile(null);
      setSaveTheDatePhotoUrl(null);
      return;
    }

    const localUrl = URL.createObjectURL(file);
    saveTheDateBlobUrlRef.current = localUrl;
    setSaveTheDatePhotoFile(file);
    setSaveTheDatePhotoUrl(localUrl);
    uploadPhotoMutation.mutate({ file, type: "save-the-date" });
  };

  const handleDigitalInvitationPhotoSelected = (file: File | null) => {
    if (digitalInvitationBlobUrlRef.current) {
      URL.revokeObjectURL(digitalInvitationBlobUrlRef.current);
      digitalInvitationBlobUrlRef.current = null;
    }

    if (!file) {
      setDigitalInvitationPhotoFile(null);
      setDigitalInvitationPhotoUrl(null);
      return;
    }

    const localUrl = URL.createObjectURL(file);
    digitalInvitationBlobUrlRef.current = localUrl;
    setDigitalInvitationPhotoFile(file);
    setDigitalInvitationPhotoUrl(localUrl);
    uploadPhotoMutation.mutate({ file, type: "digital-invitation" });
  };

  // Save customizations mutation
  const saveCustomizationsMutation = useMutation({
    mutationFn: async () => {
      if (!profileId) throw new Error("Profile ID required");

      // Upload photos if new files were selected
      let finalSaveTheDateUrl = saveTheDatePhotoUrl;
      let finalDigitalInvitationUrl = digitalInvitationPhotoUrl;

      if (saveTheDatePhotoFile) {
        const result = await uploadPhotoMutation.mutateAsync({
          file: saveTheDatePhotoFile,
          type: "save-the-date",
        });
        finalSaveTheDateUrl = result.url;
      }

      if (digitalInvitationPhotoFile) {
        const result = await uploadPhotoMutation.mutateAsync({
          file: digitalInvitationPhotoFile,
          type: "digital-invitation",
        });
        finalDigitalInvitationUrl = result.url;
      }

      // Save customizations
      const r = await authedFetch("/api/invitation-customizations", {
        method: "POST",
        body: JSON.stringify({
          profileId,
          primaryColor,
          colorPalette: autoGeneratedPalette,
          customColors,
          selectedPalette,
          backgroundColor,
          saveTheDatePhotoUrl: finalSaveTheDateUrl,
          digitalInvitationPhotoUrl: finalDigitalInvitationUrl,
          selectedFont: saveTheDateFont,
          saveTheDateFont,
          digitalInvitationFont,
          selectedLayout,
          backgroundImageUrl,
          textOverrides,
        }),
      });

      if (!r.ok) throw new Error("Failed to save");
      return r.json();
    },
    onSuccess: () => {
      toast({
        title: "Saved",
        description: "Your invitation customizations have been saved.",
      });
      setSaveTheDatePhotoFile(null);
      setDigitalInvitationPhotoFile(null);
    },
    onError: () => {
      toast({
        title: "Error",
        description: "Failed to save customizations",
        variant: "destructive",
      });
    },
  });

  // Auto-save customizations when any setting changes.
  // Photo uploads are kicked off directly from the selection handler; once the
  // upload finishes, `saveTheDatePhotoUrl` / `digitalInvitationPhotoUrl` flips
  // from a blob: URL to the server URL and this effect persists it. We skip
  // saving while a blob: URL is in flight so we don't write a non-shareable
  // URL to the database.
  useEffect(() => {
    if (!profileId) return;
    if (saveTheDatePhotoUrl?.startsWith("blob:")) return;
    if (digitalInvitationPhotoUrl?.startsWith("blob:")) return;

    const timer = setTimeout(async () => {
      try {
        const r = await authedFetch("/api/invitation-customizations", {
          method: "POST",
          body: JSON.stringify({
            profileId,
            primaryColor,
            colorPalette: autoGeneratedPalette,
            customColors,
            selectedPalette,
            backgroundColor,
            saveTheDatePhotoUrl,
            digitalInvitationPhotoUrl,
            selectedFont: saveTheDateFont,
            saveTheDateFont,
            digitalInvitationFont,
            selectedLayout,
            backgroundImageUrl,
            textOverrides,
          }),
        });

        if (!r.ok) {
          const error = await r.json().catch(() => ({ error: "Unknown error" }));
          console.error("Auto-save failed:", error);
        }
      } catch (error) {
        console.error("Auto-save error:", error);
      }
    }, 1000); // Debounce for 1 second

    return () => clearTimeout(timer);
  }, [profileId, primaryColor, autoGeneratedPalette, customColors, selectedPalette, backgroundColor, saveTheDatePhotoUrl, digitalInvitationPhotoUrl, saveTheDateFont, digitalInvitationFont, selectedLayout, backgroundImageUrl, textOverrides, authedFetch]);

  // PDF download functions
  const downloadPDF = async (refElement: HTMLDivElement | null, filename: string) => {
    if (!refElement) {
      toast({
        title: "Error",
        description: "Preview not found",
        variant: "destructive",
      });
      return;
    }

    try {
      setIsDownloadingPDF(true);

      // Dynamically import html2canvas and jsPDF
      const html2canvas = (await import("html2canvas")).default;
      const jsPDF = (await import("jspdf")).jsPDF;

      // Create canvas from the preview element
      const canvas = await html2canvas(refElement, {
        backgroundColor: backgroundColor || "#FFFFFF",
        scale: 2,
        useCORS: true,
        allowTaint: true,
      });

      // Create PDF with A4 dimensions
      const pdf = new jsPDF({
        orientation: "portrait",
        unit: "mm",
        format: "a4",
      });

      const imgWidth = 210; // A4 width in mm
      const imgHeight = (canvas.height * imgWidth) / canvas.width;

      const imgData = canvas.toDataURL("image/png");
      pdf.addImage(imgData, "PNG", 0, 0, imgWidth, imgHeight);

      pdf.save(filename);

      toast({
        title: "Downloaded",
        description: "Your invitation PDF has been downloaded.",
      });
    } catch (error) {
      console.error("PDF generation error:", error);
      toast({
        title: "Error",
        description: "Failed to generate PDF. Please try again.",
        variant: "destructive",
      });
    } finally {
      setIsDownloadingPDF(false);
    }
  };

  if (!profileId) {
    return <div className="p-4 text-center">Profile not found</div>;
  }

  // Provide default wedding profile data while loading
  const defaultWeddingProfile: WeddingProfileData = {
    id: profileId,
    partner1Name: "Partner 1",
    partner2Name: "Partner 2",
    weddingDate: new Date().toISOString(),
    ceremonyTime: "3:00 PM",
    receptionTime: "5:00 PM",
    venue: "TBD",
    location: "TBD",
  };

  const displayWeddingProfile = weddingProfile || defaultWeddingProfile;
  const displayPalette = customColors
    ? { ...autoGeneratedPalette, ...customColors }
    : autoGeneratedPalette;

  return (
    <div className="max-w-7xl mx-auto p-4 space-y-6">
      <div>
        <h1 className="text-3xl font-serif font-bold">Invitation Customization</h1>
        <p className="text-muted-foreground mt-1">
          Customize your Save the Date and Digital Invitation designs
        </p>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
        {/* Left Panel - Customization Controls */}
        <div className="lg:col-span-1 space-y-4 max-h-[90vh] overflow-y-auto">
          <PhotoUploadSection
            onSaveTheDatePhotoChange={handleSaveTheDatePhotoSelected}
            onDigitalInvitationPhotoChange={handleDigitalInvitationPhotoSelected}
            saveTheDatePreviewUrl={saveTheDatePhotoUrl}
            digitalInvitationPreviewUrl={digitalInvitationPhotoUrl}
            isLoading={uploadPhotoMutation.isPending || saveCustomizationsMutation.isPending}
          />

          <ColorSystemSection
            primaryColor={primaryColor}
            onPrimaryColorChange={setPrimaryColor}
            autoGeneratedPalette={autoGeneratedPalette}
            customColors={customColors}
            onCustomColorsChange={setCustomColors}
            selectedPalette={selectedPalette}
            onPaletteSelect={setSelectedPalette}
            backgroundColor={backgroundColor}
            onBackgroundColorChange={setBackgroundColor}
          />

          <DesignOptionsSection
            saveTheDateFont={saveTheDateFont}
            onSaveTheDateFontChange={setSaveTheDateFont}
            digitalInvitationFont={digitalInvitationFont}
            onDigitalInvitationFontChange={setDigitalInvitationFont}
            selectedLayout={selectedLayout}
            onLayoutChange={setSelectedLayout}
            backgroundColor={backgroundColor}
            onBackgroundColorChange={setBackgroundColor}
          />
        </div>

        {/* Right Panel - Preview */}
        <div className="lg:col-span-2">
          <Card className="h-[90vh] flex flex-col">
            <div className="border-b p-4">
              <Tabs
                value={previewTab}
                onValueChange={(value) =>
                  setPreviewTab(value as PreviewTab)
                }
              >
                <TabsList className="grid w-full grid-cols-2">
                  <TabsTrigger value="saveTheDate">Save the Date</TabsTrigger>
                  <TabsTrigger value="digitalInvitation">
                    Digital Invitation
                  </TabsTrigger>
                </TabsList>
              </Tabs>
            </div>

            <div className="flex-1 overflow-auto p-4">
              {previewTab === "saveTheDate" ? (
                <SaveTheDatePreview
                  ref={saveTheDatePreviewRef}
                  photoUrl={saveTheDatePhotoUrl}
                  weddingDate={displayWeddingProfile.weddingDate}
                  colors={displayPalette}
                  font={saveTheDateFont}
                  backgroundColor={backgroundColor}
                  partner1Name={displayWeddingProfile.partner1Name}
                  partner2Name={displayWeddingProfile.partner2Name}
                  location={displayWeddingProfile.location}
                  textOverrides={textOverrides}
                  onTextOverridesChange={setTextOverrides}
                />
              ) : (
                <DigitalInvitationPreview
                  ref={digitalInvitationPreviewRef}
                  photoUrl={digitalInvitationPhotoUrl}
                  venue={displayWeddingProfile.venue}
                  location={displayWeddingProfile.location}
                  ceremonyTime={displayWeddingProfile.ceremonyTime}
                  receptionTime={displayWeddingProfile.receptionTime}
                  guestName={user?.firstName || "Guest Name"}
                  colors={displayPalette}
                  font={digitalInvitationFont}
                  backgroundColor={backgroundColor}
                  partner1Name={displayWeddingProfile.partner1Name}
                  partner2Name={displayWeddingProfile.partner2Name}
                  weddingDate={displayWeddingProfile.weddingDate}
                  textOverrides={textOverrides}
                  onTextOverridesChange={setTextOverrides}
                />
              )}
            </div>

            <div className="border-t p-4">
              <Button
                onClick={() =>
                  downloadPDF(
                    previewTab === "saveTheDate"
                      ? saveTheDatePreviewRef.current
                      : digitalInvitationPreviewRef.current,
                    previewTab === "saveTheDate"
                      ? "save-the-date.pdf"
                      : "digital-invitation.pdf"
                  )
                }
                disabled={isDownloadingPDF}
                className="w-full"
              >
                {isDownloadingPDF ? (
                  <>
                    <Loader2 className="h-4 w-4 mr-2 animate-spin" />
                    Generating PDF...
                  </>
                ) : (
                  <>
                    <Download className="h-4 w-4 mr-2" />
                    Download as PDF
                  </>
                )}
              </Button>
            </div>
          </Card>
        </div>
      </div>
    </div>
  );
}
