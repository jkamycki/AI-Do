import { useState, useEffect, useRef, useCallback } from "react";
import { useMutation, useQuery } from "@tanstack/react-query";
import { useAuth, useUser } from "@clerk/react";
import { useRoute } from "wouter";
import { useWorkspace } from "@/contexts/WorkspaceContext";
import { PhotoUploadSection } from "@/components/InvitationCustomization/PhotoUploadSection";
import { ColorSystemSection } from "@/components/InvitationCustomization/ColorSystemSection";
import { DesignOptionsSection } from "@/components/InvitationCustomization/DesignOptionsSection";
import { SaveTheDatePreview } from "@/components/InvitationCustomization/SaveTheDatePreview";
import { DigitalInvitationPreview } from "@/components/InvitationCustomization/DigitalInvitationPreview";
import { Card } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { useToast } from "@/hooks/use-toast";
import { Tabs, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { Download, Loader2, RotateCcw } from "lucide-react";
import type {
  InvitationCustomization,
  ColorPalette,
  PreviewTab,
  WeddingProfileData,
  TextOverrides,
} from "@/types/invitations";

interface RouteParams {
  profileId?: string;
}

interface InvitationCustomizationProps {
  profileId?: number;
}

export default function InvitationCustomizationPage({ profileId: propProfileId }: InvitationCustomizationProps = {}) {
  const { getToken } = useAuth();
  const { user } = useUser();
  const { toast } = useToast();
  const { activeWorkspace } = useWorkspace();
  const [, params] = useRoute<RouteParams>("/guests/:profileId");

  const profileId = propProfileId || (params?.profileId
    ? parseInt(params.profileId)
    : activeWorkspace?.profileId);
  const [previewTab, setPreviewTab] = useState<PreviewTab>("saveTheDate");

  // ── Shared brand-color state ──────────────────────────────────────────────
  // Default palette matches the dark-navy + gold aesthetic shown in the app.
  const [primaryColor, setPrimaryColor] = useState("#D4A017");
  const [autoGeneratedPalette, setAutoGeneratedPalette] = useState<ColorPalette>({
    primary: "#D4A017",
    secondary: "#F5C842",
    accent: "#D4A017",
    neutral: "#E8E0D0",
  });
  const [customColors, setCustomColors] = useState<Partial<ColorPalette> | null>(null);
  const [selectedPalette, setSelectedPalette] = useState<string | null>(null);
  const [backgroundImageUrl, setBackgroundImageUrl] = useState<string | null>(null);

  // ── Save the Date ─────────────────────────────────────────────────────────
  const [saveTheDatePhotoFile, setSaveTheDatePhotoFile] = useState<File | null>(null);
  const [saveTheDatePhotoUrl, setSaveTheDatePhotoUrl] = useState<string | null>(null);
  const [saveTheDateFont, setSaveTheDateFont] = useState("Georgia");
  const [saveTheDateLayout, setSaveTheDateLayout] = useState("classic");
  const [saveTheDateBackground, setSaveTheDateBackground] = useState<string | null>("#1E1A2E");
  const [stdTextOverrides, setStdTextOverrides] = useState<TextOverrides>({});

  // ── Digital Invitation ────────────────────────────────────────────────────
  const [digitalInvitationPhotoFile, setDigitalInvitationPhotoFile] = useState<File | null>(null);
  const [digitalInvitationPhotoUrl, setDigitalInvitationPhotoUrl] = useState<string | null>(null);
  const [digitalInvitationFont, setDigitalInvitationFont] = useState("Georgia");
  const [digitalInvitationLayout, setDigitalInvitationLayout] = useState("classic");
  const [digitalInvitationBackground, setDigitalInvitationBackground] = useState<string | null>("#1E1A2E");
  const [digTextOverrides, setDigTextOverrides] = useState<TextOverrides>({});

  // ── Misc ──────────────────────────────────────────────────────────────────
  const skipNextAutoSave = useRef(true);
  // Holds the latest values for the unmount-save so we don't capture stale closures
  const latestValuesRef = useRef({
    profileId,
    primaryColor,
    autoGeneratedPalette,
    customColors,
    selectedPalette,
    saveTheDatePhotoUrl,
    saveTheDateFont,
    saveTheDateLayout,
    saveTheDateBackground,
    stdTextOverrides,
    digitalInvitationPhotoUrl,
    digitalInvitationFont,
    digitalInvitationLayout,
    digitalInvitationBackground,
    digTextOverrides,
    backgroundImageUrl,
  });
  const [isDownloadingPDF, setIsDownloadingPDF] = useState(false);
  const saveTheDatePreviewRef = useRef<HTMLDivElement>(null);
  const digitalInvitationPreviewRef = useRef<HTMLDivElement>(null);
  const saveTheDateBlobUrlRef = useRef<string | null>(null);
  const digitalInvitationBlobUrlRef = useRef<string | null>(null);

  // Keep ref current on every render so the unmount effect always has latest values
  latestValuesRef.current = {
    profileId,
    primaryColor,
    autoGeneratedPalette,
    customColors,
    selectedPalette,
    saveTheDatePhotoUrl,
    saveTheDateFont,
    saveTheDateLayout,
    saveTheDateBackground,
    stdTextOverrides,
    digitalInvitationPhotoUrl,
    digitalInvitationFont,
    digitalInvitationLayout,
    digitalInvitationBackground,
    digTextOverrides,
    backgroundImageUrl,
  };

  useEffect(() => {
    return () => {
      if (saveTheDateBlobUrlRef.current) URL.revokeObjectURL(saveTheDateBlobUrlRef.current);
      if (digitalInvitationBlobUrlRef.current) URL.revokeObjectURL(digitalInvitationBlobUrlRef.current);
    };
  }, []);

  // ── Save on unmount so navigating away never drops unsaved changes ──────────
  useEffect(() => {
    return () => {
      const v = latestValuesRef.current;
      if (!v.profileId) return;
      if (v.saveTheDatePhotoUrl?.startsWith("blob:")) return;
      if (v.digitalInvitationPhotoUrl?.startsWith("blob:")) return;
      getToken().then((token) => {
        fetch("/api/invitation-customizations", {
          method: "POST",
          credentials: "include",
          headers: {
            "Content-Type": "application/json",
            ...(token ? { Authorization: `Bearer ${token}` } : {}),
          },
          body: JSON.stringify({
            profileId: v.profileId,
            primaryColor: v.primaryColor,
            colorPalette: v.autoGeneratedPalette,
            customColors: v.customColors,
            selectedPalette: v.selectedPalette,
            backgroundColor: v.saveTheDateBackground,
            selectedFont: v.saveTheDateFont,
            selectedLayout: v.saveTheDateLayout,
            saveTheDatePhotoUrl: v.saveTheDatePhotoUrl,
            digitalInvitationPhotoUrl: v.digitalInvitationPhotoUrl,
            saveTheDateFont: v.saveTheDateFont,
            digitalInvitationFont: v.digitalInvitationFont,
            saveTheDateLayout: v.saveTheDateLayout,
            digitalInvitationLayout: v.digitalInvitationLayout,
            saveTheDateBackground: v.saveTheDateBackground,
            digitalInvitationBackground: v.digitalInvitationBackground,
            backgroundImageUrl: v.backgroundImageUrl,
            textOverrides: { ...v.stdTextOverrides, ...v.digTextOverrides },
          }),
        }).catch(() => {});
      }).catch(() => {});
    };
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, []); // empty deps — fires only on unmount

  const authedFetch = async (url: string, init: RequestInit = {}) => {
    const token = await getToken();
    return fetch(url, {
      ...init,
      credentials: "include",
      headers: {
        "Content-Type": "application/json",
        ...(init.headers ?? {}),
        ...(token ? { Authorization: `Bearer ${token}` } : {}),
      },
    });
  };

  // ── Queries ───────────────────────────────────────────────────────────────
  const { data: weddingProfile } = useQuery({
    queryKey: ["wedding-profile", profileId],
    queryFn: async () => {
      if (!profileId) return null;
      const r = await authedFetch(`/api/profile`);
      if (!r.ok) throw new Error("Failed to fetch profile");
      return r.json() as Promise<WeddingProfileData>;
    },
    enabled: !!profileId,
    retry: 1,
  });

  const { data: customization } = useQuery({
    queryKey: ["invitation-customizations", profileId],
    queryFn: async () => {
      if (!profileId) return null;
      const r = await authedFetch(`/api/invitation-customizations?profileId=${profileId}`);
      if (!r.ok) throw new Error("Failed to fetch customizations");
      return r.json() as Promise<InvitationCustomization>;
    },
    enabled: !!profileId,
  });

  // ── Load DB data into state ───────────────────────────────────────────────
  useEffect(() => {
    if (customization) {
      skipNextAutoSave.current = true;

      // Shared
      setPrimaryColor(customization.primaryColor);
      setAutoGeneratedPalette(customization.colorPalette);
      setCustomColors(customization.customColors);
      setSelectedPalette(customization.selectedPalette);
      setBackgroundImageUrl(customization.backgroundImageUrl);

      // Per-design photos
      setSaveTheDatePhotoUrl(customization.saveTheDatePhotoUrl);
      setDigitalInvitationPhotoUrl(customization.digitalInvitationPhotoUrl);

      // Per-design fonts
      setSaveTheDateFont(customization.saveTheDateFont || customization.selectedFont || "Georgia");
      setDigitalInvitationFont(customization.digitalInvitationFont || customization.selectedFont || "Georgia");

      // Per-design layout
      setSaveTheDateLayout(customization.saveTheDateLayout || customization.selectedLayout || "classic");
      setDigitalInvitationLayout(customization.digitalInvitationLayout || customization.selectedLayout || "classic");

      // Per-design background — default to dark navy if nothing was ever saved
      setSaveTheDateBackground(customization.saveTheDateBackground ?? customization.backgroundColor ?? "#1E1A2E");
      setDigitalInvitationBackground(customization.digitalInvitationBackground ?? customization.backgroundColor ?? "#1E1A2E");

      // Split text overrides by prefix
      const allOverrides = customization.textOverrides || {};
      const stdOverrides: TextOverrides = {};
      const digOverrides: TextOverrides = {};
      for (const [key, val] of Object.entries(allOverrides)) {
        if (key.startsWith("std:")) stdOverrides[key] = val;
        else if (key.startsWith("dig:")) digOverrides[key] = val;
      }
      setStdTextOverrides(stdOverrides);
      setDigTextOverrides(digOverrides);
    }
  }, [customization]);

  // ── Photo upload mutation ─────────────────────────────────────────────────
  const uploadPhotoMutation = useMutation({
    mutationFn: async ({ file, type }: { file: File; type: "save-the-date" | "digital-invitation" }) => {
      const formData = new FormData();
      formData.append("file", file);
      const token = await getToken();
      const r = await fetch(`/api/invitation-customizations/upload?type=${type}`, {
        method: "POST",
        credentials: "include",
        headers: { ...(token ? { Authorization: `Bearer ${token}` } : {}) },
        body: formData,
      });
      if (!r.ok) {
        const error = await r.json().catch(() => ({ error: "Unknown error" }));
        throw new Error(error.error || `Upload failed with status ${r.status}`);
      }
      return r.json() as Promise<{ url: string }>;
    },
    onSuccess: (data, variables) => {
      if (variables.type === "save-the-date") {
        if (saveTheDateBlobUrlRef.current) { URL.revokeObjectURL(saveTheDateBlobUrlRef.current); saveTheDateBlobUrlRef.current = null; }
        setSaveTheDatePhotoUrl(data.url);
        setSaveTheDatePhotoFile(null);
      } else {
        if (digitalInvitationBlobUrlRef.current) { URL.revokeObjectURL(digitalInvitationBlobUrlRef.current); digitalInvitationBlobUrlRef.current = null; }
        setDigitalInvitationPhotoUrl(data.url);
        setDigitalInvitationPhotoFile(null);
      }
    },
    onError: (error) => {
      toast({ title: "Upload Failed", description: error instanceof Error ? error.message : "Failed to upload photo", variant: "destructive" });
    },
  });

  // ── Photo selection handlers ──────────────────────────────────────────────
  const handleSaveTheDatePhotoSelected = (file: File | null) => {
    if (saveTheDateBlobUrlRef.current) { URL.revokeObjectURL(saveTheDateBlobUrlRef.current); saveTheDateBlobUrlRef.current = null; }
    if (!file) { setSaveTheDatePhotoFile(null); setSaveTheDatePhotoUrl(null); return; }
    const localUrl = URL.createObjectURL(file);
    saveTheDateBlobUrlRef.current = localUrl;
    setSaveTheDatePhotoFile(file);
    setSaveTheDatePhotoUrl(localUrl);
    uploadPhotoMutation.mutate({ file, type: "save-the-date" });
  };

  const handleDigitalInvitationPhotoSelected = (file: File | null) => {
    if (digitalInvitationBlobUrlRef.current) { URL.revokeObjectURL(digitalInvitationBlobUrlRef.current); digitalInvitationBlobUrlRef.current = null; }
    if (!file) { setDigitalInvitationPhotoFile(null); setDigitalInvitationPhotoUrl(null); return; }
    const localUrl = URL.createObjectURL(file);
    digitalInvitationBlobUrlRef.current = localUrl;
    setDigitalInvitationPhotoFile(file);
    setDigitalInvitationPhotoUrl(localUrl);
    uploadPhotoMutation.mutate({ file, type: "digital-invitation" });
  };

  // ── Save mutation (manual) ────────────────────────────────────────────────
  const saveCustomizationsMutation = useMutation({
    mutationFn: async () => {
      if (!profileId) throw new Error("Profile ID required");
      let finalSaveTheDateUrl = saveTheDatePhotoUrl;
      let finalDigitalInvitationUrl = digitalInvitationPhotoUrl;
      if (saveTheDatePhotoFile) {
        const result = await uploadPhotoMutation.mutateAsync({ file: saveTheDatePhotoFile, type: "save-the-date" });
        finalSaveTheDateUrl = result.url;
      }
      if (digitalInvitationPhotoFile) {
        const result = await uploadPhotoMutation.mutateAsync({ file: digitalInvitationPhotoFile, type: "digital-invitation" });
        finalDigitalInvitationUrl = result.url;
      }
      const r = await authedFetch("/api/invitation-customizations", {
        method: "POST",
        body: JSON.stringify(buildPayload(finalSaveTheDateUrl, finalDigitalInvitationUrl)),
      });
      if (!r.ok) throw new Error("Failed to save");
      return r.json();
    },
    onSuccess: () => {
      toast({ title: "Saved", description: "Your invitation customizations have been saved." });
      setSaveTheDatePhotoFile(null);
      setDigitalInvitationPhotoFile(null);
    },
    onError: () => {
      toast({ title: "Error", description: "Failed to save customizations", variant: "destructive" });
    },
  });

  // ── Reset to brand colors ──────────────────────────────────────────────────
  const BRAND_PALETTE: ColorPalette = {
    primary: "#D4A017",
    secondary: "#F5C842",
    accent: "#D4A017",
    neutral: "#E8E0D0",
  };
  const handleResetToBrandColors = useCallback(() => {
    setPrimaryColor("#D4A017");
    setAutoGeneratedPalette(BRAND_PALETTE);
    setCustomColors(null);
    setSelectedPalette(null);
    if (previewTab === "saveTheDate") {
      setSaveTheDateBackground("#1E1A2E");
      setStdTextOverrides({});
    } else {
      setDigitalInvitationBackground("#1E1A2E");
      setDigTextOverrides({});
    }
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [previewTab]);

  // ── Build save payload ────────────────────────────────────────────────────
  const buildPayload = (stdPhotoUrl = saveTheDatePhotoUrl, digPhotoUrl = digitalInvitationPhotoUrl) => ({
    profileId,
    primaryColor,
    colorPalette: autoGeneratedPalette,
    customColors,
    selectedPalette,
    // Keep the legacy fields for backward compat
    backgroundColor: saveTheDateBackground,
    selectedFont: saveTheDateFont,
    selectedLayout: saveTheDateLayout,
    // Per-design fields
    saveTheDatePhotoUrl: stdPhotoUrl,
    digitalInvitationPhotoUrl: digPhotoUrl,
    saveTheDateFont,
    digitalInvitationFont,
    saveTheDateLayout,
    digitalInvitationLayout,
    saveTheDateBackground,
    digitalInvitationBackground,
    backgroundImageUrl,
    // Merge both sets of overrides back into one object for the DB
    textOverrides: { ...stdTextOverrides, ...digTextOverrides },
  });

  // ── Auto-save (debounced 1s, skip initial load) ───────────────────────────
  useEffect(() => {
    if (!profileId) return;
    if (skipNextAutoSave.current) { skipNextAutoSave.current = false; return; }
    if (saveTheDatePhotoUrl?.startsWith("blob:")) return;
    if (digitalInvitationPhotoUrl?.startsWith("blob:")) return;

    const timer = setTimeout(async () => {
      try {
        const r = await authedFetch("/api/invitation-customizations", {
          method: "POST",
          body: JSON.stringify(buildPayload()),
        });
        if (!r.ok) {
          const error = await r.json().catch(() => ({ error: "Unknown error" }));
          console.error("Auto-save failed:", error);
        }
      } catch (error) {
        console.error("Auto-save error:", error);
      }
    }, 1000);

    return () => clearTimeout(timer);
  }, [
    profileId,
    primaryColor, autoGeneratedPalette, customColors, selectedPalette,
    saveTheDatePhotoUrl, saveTheDateFont, saveTheDateLayout, saveTheDateBackground, stdTextOverrides,
    digitalInvitationPhotoUrl, digitalInvitationFont, digitalInvitationLayout, digitalInvitationBackground, digTextOverrides,
    backgroundImageUrl,
  ]);

  // ── PDF download ──────────────────────────────────────────────────────────
  const downloadPDF = async (refElement: HTMLDivElement | null, filename: string) => {
    if (!refElement) { toast({ title: "Error", description: "Preview not found", variant: "destructive" }); return; }
    try {
      setIsDownloadingPDF(true);
      const html2canvas = (await import("html2canvas")).default;
      const jsPDF = (await import("jspdf")).jsPDF;
      const bgColor = previewTab === "saveTheDate" ? (saveTheDateBackground || "#FFFFFF") : (digitalInvitationBackground || "#FFFFFF");
      const canvas = await html2canvas(refElement, { backgroundColor: bgColor, scale: 2, useCORS: true, allowTaint: true });
      const pdf = new jsPDF({ orientation: "portrait", unit: "mm", format: "a4" });
      const imgWidth = 210;
      const imgHeight = (canvas.height * imgWidth) / canvas.width;
      pdf.addImage(canvas.toDataURL("image/png"), "PNG", 0, 0, imgWidth, imgHeight);
      pdf.save(filename);
      toast({ title: "Downloaded", description: "Your invitation PDF has been downloaded." });
    } catch (error) {
      console.error("PDF generation error:", error);
      toast({ title: "Error", description: "Failed to generate PDF. Please try again.", variant: "destructive" });
    } finally {
      setIsDownloadingPDF(false);
    }
  };

  if (!profileId) return <div className="p-4 text-center">Profile not found</div>;

  const defaultWeddingProfile: WeddingProfileData = {
    id: profileId,
    partner1Name: "Partner 1",
    partner2Name: "Partner 2",
    weddingDate: new Date().toISOString(),
    ceremonyTime: "3:00 PM",
    receptionTime: "5:00 PM",
    venue: "TBD",
    location: "TBD",
  };

  const displayWeddingProfile = weddingProfile || defaultWeddingProfile;
  const displayPalette = customColors ? { ...autoGeneratedPalette, ...customColors } : autoGeneratedPalette;
  const isSTD = previewTab === "saveTheDate";

  return (
    <div className="max-w-7xl mx-auto p-4 space-y-6">
      <div>
        <h1 className="text-3xl font-serif font-bold">Invitation Customization</h1>
        <p className="text-muted-foreground mt-1">
          Customize your Save the Date and Digital Invitation designs
        </p>
      </div>

      <div className="grid grid-cols-1 lg:grid-cols-3 gap-6">
        {/* Left Panel — context-aware controls for the active design */}
        <div className="lg:col-span-1 space-y-4 max-h-[90vh] overflow-y-auto">
          {/* Photo — shows only the slot for the active design */}
          <PhotoUploadSection
            mode={previewTab}
            onSaveTheDatePhotoChange={handleSaveTheDatePhotoSelected}
            onDigitalInvitationPhotoChange={handleDigitalInvitationPhotoSelected}
            saveTheDatePreviewUrl={saveTheDatePhotoUrl}
            digitalInvitationPreviewUrl={digitalInvitationPhotoUrl}
            isLoading={uploadPhotoMutation.isPending || saveCustomizationsMutation.isPending}
          />

          {/* Brand-color palette — shared, but each design uses it independently */}
          <ColorSystemSection
            primaryColor={primaryColor}
            onPrimaryColorChange={setPrimaryColor}
            autoGeneratedPalette={autoGeneratedPalette}
            customColors={customColors}
            onCustomColorsChange={setCustomColors}
            selectedPalette={selectedPalette}
            onPaletteSelect={setSelectedPalette}
            backgroundColor={isSTD ? saveTheDateBackground : digitalInvitationBackground}
            onBackgroundColorChange={isSTD ? setSaveTheDateBackground : setDigitalInvitationBackground}
          />

          {/* Layout / background — per-design, switches with the preview tab */}
          <DesignOptionsSection
            mode={previewTab}
            selectedLayout={isSTD ? saveTheDateLayout : digitalInvitationLayout}
            onLayoutChange={isSTD ? setSaveTheDateLayout : setDigitalInvitationLayout}
            backgroundColor={isSTD ? saveTheDateBackground : digitalInvitationBackground}
            onBackgroundColorChange={isSTD ? setSaveTheDateBackground : setDigitalInvitationBackground}
            colors={displayPalette}
          />
        </div>

        {/* Right Panel — Preview */}
        <div className="lg:col-span-2">
          <Card className="h-[90vh] flex flex-col">
            <div className="border-b p-4 space-y-3">
              {/* Reset button — clears only the active design's overrides */}
              <div className="flex items-center justify-between">
                <p className="text-xs text-muted-foreground">
                  Preview — click any element to customise
                </p>
                <Button
                  type="button"
                  size="sm"
                  variant="outline"
                  className="h-8 gap-1.5 text-xs"
                  onClick={handleResetToBrandColors}
                  title={`Reset ${isSTD ? "Save the Date" : "Digital Invitation"} to brand colours`}
                >
                  <RotateCcw className="h-3.5 w-3.5" />
                  Reset to Brand Colors
                </Button>
              </div>

              <Tabs
                value={previewTab}
                onValueChange={(value) => setPreviewTab(value as PreviewTab)}
              >
                <TabsList className="grid w-full grid-cols-2">
                  <TabsTrigger value="saveTheDate">Save the Date</TabsTrigger>
                  <TabsTrigger value="digitalInvitation">Digital Invitation</TabsTrigger>
                </TabsList>
              </Tabs>
            </div>

            <div className="flex-1 overflow-auto p-4">
              {isSTD ? (
                <SaveTheDatePreview
                  ref={saveTheDatePreviewRef}
                  photoUrl={saveTheDatePhotoUrl}
                  weddingDate={displayWeddingProfile.weddingDate}
                  colors={displayPalette}
                  font={saveTheDateFont}
                  layout={saveTheDateLayout}
                  backgroundColor={saveTheDateBackground}
                  partner1Name={displayWeddingProfile.partner1Name}
                  partner2Name={displayWeddingProfile.partner2Name}
                  location={displayWeddingProfile.location}
                  textOverrides={stdTextOverrides}
                  onTextOverridesChange={setStdTextOverrides}
                />
              ) : (
                <DigitalInvitationPreview
                  ref={digitalInvitationPreviewRef}
                  photoUrl={digitalInvitationPhotoUrl}
                  venue={displayWeddingProfile.venue}
                  location={displayWeddingProfile.location}
                  ceremonyTime={displayWeddingProfile.ceremonyTime}
                  receptionTime={displayWeddingProfile.receptionTime}
                  guestName={user?.firstName || "Guest Name"}
                  colors={displayPalette}
                  font={digitalInvitationFont}
                  layout={digitalInvitationLayout}
                  backgroundColor={digitalInvitationBackground}
                  partner1Name={displayWeddingProfile.partner1Name}
                  partner2Name={displayWeddingProfile.partner2Name}
                  weddingDate={displayWeddingProfile.weddingDate}
                  textOverrides={digTextOverrides}
                  onTextOverridesChange={setDigTextOverrides}
                />
              )}
            </div>

            <div className="border-t p-4">
              <Button
                onClick={() =>
                  downloadPDF(
                    isSTD ? saveTheDatePreviewRef.current : digitalInvitationPreviewRef.current,
                    isSTD ? "save-the-date.pdf" : "digital-invitation.pdf"
                  )
                }
                disabled={isDownloadingPDF}
                className="w-full"
              >
                {isDownloadingPDF ? (
                  <><Loader2 className="h-4 w-4 mr-2 animate-spin" />Generating PDF...</>
                ) : (
                  <><Download className="h-4 w-4 mr-2" />Download as PDF</>
                )}
              </Button>
            </div>
          </Card>
        </div>
      </div>
    </div>
  );
}
